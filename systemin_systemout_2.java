import java.util.*;

public class systemin_systemout_2 {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int u = sc.nextInt();
        double k = sc.nextDouble();
        sc.nextLine();
        String c = sc.nextLine();
        System.out.println("String:" + c + "\n" + "Double:" + k + "\n" + "Int:" + u);
        sc.close();

    }
}
