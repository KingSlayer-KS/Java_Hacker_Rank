import java.util.*;

public class systemin_systemout {

    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int u = sc.nextInt();
        int c = sc.nextInt();
        int k = sc.nextInt();
        System.out.println(u + "\n" + c + "\n" + k);
        sc.close();
    }
}
